// --- dkim signer table ------------------------------------------------------

func writeDkimTable(b *strings.Builder, ids []DkimIdentity) {
	// Always declare the table so the smtp_client_message_sending handler
	// can index it unconditionally — leaving it undeclared turns a no-op
	// "no DKIM configured" path into a runtime "undefined global" error
	// the first time kumomta tries to deliver a message.
	if len(ids) == 0 {
		b.WriteString("-- ===== dkim signers (none configured) =====\n")
		b.WriteString("local DKIM_BY_DOMAIN = {}\n\n")
		return
	}
	b.WriteString("-- ===== dkim signers =====\n")
	b.WriteString("local DKIM_BY_DOMAIN = {}\n")
	cp := append([]DkimIdentity(nil), ids...)
	sort.Slice(cp, func(i, j int) bool { return cp[i].Domain < cp[j].Domain })
	for _, d := range cp {
		// Translate ed25519/rsa-1024/rsa-2048/rsa-4096 → kumomta's algorithm names.
		algo := d.Algorithm
		switch algo {
		case "ed25519":
			algo = "ed25519"
		case "rsa-1024", "rsa-2048", "rsa-4096", "rsa":
			algo = "sha256"
		}
		fmt.Fprintf(b, "DKIM_BY_DOMAIN[%s] = { selector = %s, key = %s, algo = %s }\n",
			MustLuaString(d.Domain),
			MustLuaString(d.Selector),
			MustLuaString(d.KeyPath),
			MustLuaString(algo),
		)
	}
	b.WriteString("\n")
}

// --- suppression lookup -----------------------------------------------------
//
// At ~1M messages/day with multi-million-entry suppression lists, embedding
// every entry into init.lua becomes prohibitive: the rendered file grows
// past 50 MB, every /v1/policy/apply re-parses it, and every kumod replica
// keeps a private copy in Lua state. We instead defer to Redis (already in
// the deployment for log streaming) at message-receive time. A
// kumo.memoize TTL cache absorbs bursts to the same recipient without
// hitting Redis on every message.
//
// Two SETs hold the truth:
//   - kumo:supp:addr  exact addresses (envelope.recipient.email)
//   - kumo:supp:dom   exact recipient domains
//
// admin-service dual-writes both on Create/Delete and runs a startup
// resync that rebuilds the SETs from PostgreSQL — so a Redis flush, a
// fresh deployment, or drift after a network partition all repair
// themselves on the next admin-service boot.
//
// Fail-open is deliberate. If Redis is unreachable we log and let the
// message proceed; blocking legitimate mail because Redis hiccupped is
// strictly worse than letting one suppressed recipient slip through.
// The next epoch (10s) and the next resync are independent self-heals.
func writeSuppressionLookup(b *strings.Builder, gs GlobalSettings) {
	b.WriteString("-- ===== suppression list (redis-backed; SISMEMBER + memoize) =====\n")
	// is_suppressed is a forward-declared local so route_message captures
	// it as an upvalue regardless of whether Redis is configured. With
	// no Redis we leave it as the stub that returns nil; with Redis we
	// reassign inside the do-block so the connection and memoize cache
	// are kept private.
	b.WriteString("local is_suppressed = function(_rcpt, _rdom) return nil end\n")
	if gs.LogStreamRedisURL == "" {
		// No Redis configured — operator has opted out of the suppression
		// list. Stub stays in place; route_message's call is a no-op.
		b.WriteString("\n")
		return
	}
	// kumomta's redis module: same connection style the log_hook uses.
	// pool_size matches typical worker count + headroom; ttl = 15s caps
	// the staleness window for a freshly-added suppression to take
	// effect (delete is symmetric — at most 15s of one-extra-reject).
	b.WriteString(`do
  local redis = require 'redis'
  local supp_conn = redis.open {
    node = LOGSTREAM_REDIS_URL,
    pool_size = 32,
  }
  -- Memoize collapses repeat lookups (retries, multi-recipient sends,
  -- list bursts) onto a single Redis hit. capacity sized for ~15s of
  -- working set at a few hundred msg/s; tune up if your queue depth
  -- consistently exceeds it.
  local function _check(rcpt, rdom)
    local ok, hit = pcall(function()
      if supp_conn:query('SISMEMBER', 'kumo:supp:addr', rcpt) == 1 then return 'addr' end
      if supp_conn:query('SISMEMBER', 'kumo:supp:dom',  rdom) == 1 then return 'dom'  end
      return nil
    end)
    if not ok then
      kumo.log_error('suppression: redis check failed: ' .. tostring(hit))
      return nil   -- fail-open: don't block legit mail on a Redis hiccup
    end
    return hit
  end
  is_suppressed = kumo.memoize(_check, {
    name = 'suppression-check',
    ttl = '15s',
    capacity = 100000,
  })
end

`)
}

// --- the routing chain ------------------------------------------------------
//
// Both the SMTP server (smtp_server_message_received) and the HTTP injection
// API (http_message_generated) dispatch into the same Lua function so policy
// decisions stay consistent across both entry points.
func writeRoutingChain(b *strings.Builder, mcs []MailClass, rs []RoutingRule, gs GlobalSettings, whs []MailWebhook, opts RenderOptions) {
	header := gs.MailClassHeader
	if header == "" {
		header = MailClassHeaderDefault
	}

	b.WriteString("-- ===== routing chain (suppression → mail class → routing rules → DKIM) =====\n")

	// Header hygiene: submission/injection paths often omit Date and
	// Message-ID, which receivers' rspamd penalizes (MISSING_DATE,
	// MISSING_MID). Add them only when absent so relayed mail that already
	// carries them is untouched. Runs at reception (route_message is hooked
	// on both smtp_server_message_received and http_message_generated), so
	// the headers exist before the outbound DKIM signer runs and are
	// covered by the signature. The Message-ID domain is the configured
	// egress EHLO FQDN when set, else the envelope-sender domain.
	fmt.Fprintf(b, "local IRIS_MID_DOMAIN = %s\n", MustLuaString(strings.TrimSpace(gs.EgressEhloDomain)))
	b.WriteString(`local function iris_add_missing_headers(msg)
  if (msg:get_first_named_header_value('Date') or '') == '' then
    -- pcall so an unexpected os.date failure degrades to "no Date added"
    -- rather than failing the message.
    local ok, d = pcall(os.date, '!%a, %d %b %Y %H:%M:%S +0000')
    if ok and d and d ~= '' then
      msg:prepend_header('Date', d)
    end
  end
  if (msg:get_first_named_header_value('Message-ID') or '') == '' then
    local dom = IRIS_MID_DOMAIN
    if dom == '' then
      local s = msg:sender()
      dom = (s and s.domain) or 'localhost'
    end
    msg:prepend_header('Message-ID', string.format('<%s@%s>', msg:id(), dom))
  end
end

`)

	b.WriteString("local function route_message(msg)\n")
	if opts.DryRun {
		b.WriteString("  print('[DRY-RUN] route_message')\n")
	}
	b.WriteString("  iris_add_missing_headers(msg)\n")

	// 0) inbound DSN catcher. Mail arriving at any of our configured bounce
	// domains is funneled straight into the DSN_TRACKER queue (custom_lua →
	// XADD onto Redis) and skips the rest of the chain. We do this before
	// suppression because a suppression match would fire kumo.reject(550),
	// which would itself trigger another DSN — a loop. The lookup is a Lua
	// set so multi-domain mode (one entry per sender's bounce subdomain)
	// stays O(1).
	if bouncePipelineEnabled(gs) {
		b.WriteString(`  do
    local rcpt = msg:recipient()
    local rdom = (rcpt and rcpt.domain or ''):lower()
    if BOUNCE_DOMAINS[rdom] then
      msg:set_meta('queue', DSN_TRACKER)
      return
    end
  end
`)
	}

	// 0b) inbound webhook catcher. Mail addressed to a configured webhook
	// recipient (exact email or a bare-domain catch-all) is routed straight
	// to the WEBHOOK_TRACKER queue, which POSTs it to the endpoint. Checked
	// before suppression for the same loop-avoidance reason as DSNs.
	if len(enabledWebhooks(whs)) > 0 {
		b.WriteString(`  do
    local rcpt = msg:recipient()
    local email = (rcpt and rcpt.email or ''):lower()
    local rdom = (rcpt and rcpt.domain or ''):lower()
    if WEBHOOK_BY_EMAIL[email] or WEBHOOK_BY_DOMAIN[rdom] then
      msg:set_meta('queue', WEBHOOK_TRACKER)
      return
    end
  end
`)
	}

	// 1) suppression check via the memoized Redis lookup. is_suppressed is
	// declared higher in the file (writeSuppressionLookup); it returns
	// 'addr' / 'dom' on a hit, nil otherwise. No-Redis deployments get a
	// stub that always returns nil — the suppression list is opt-in.
	b.WriteString(`  do
    local rcpt = ((msg:recipient() and msg:recipient().email) or ''):lower()
    local rdom = ((msg:recipient() and msg:recipient().domain) or ''):lower()
    if is_suppressed(rcpt, rdom) then
      kumo.reject(550, 'recipient is on the suppression list')
      return
    end
  end
`)

	// 2) mail class header lookup. The class NAME becomes the tenant, so
	// kumomta's queue grouping (visible in /v1/queues) buckets one row per
	// class — `<classname>@<domain>` — instead of collapsing everything by
	// egress pool. The pool itself is recovered in get_queue_config via the
	// parallel CLASS_TO_POOL lookup. mailclass meta is also set so the log
	// hook indexes it for the Logs UI search.
	if len(mcs) > 0 {
		fmt.Fprintf(b, `  do
    local v = msg:get_first_named_header_value(%s)
    if v and v ~= '' then
      local hit = MAIL_CLASSES[v]
      if hit then
        msg:set_meta('mailclass', v)
        msg:set_meta('tenant', v)
        return
      end
    end
  end
`, MustLuaString(header))
	}

	// 3) routing rules in (priority asc, name asc) order.
	cp := append([]RoutingRule(nil), rs...)
	sort.Slice(cp, func(i, j int) bool {
		if cp[i].Priority != cp[j].Priority {
			return cp[i].Priority < cp[j].Priority
		}
		return cp[i].Name < cp[j].Name
	})
	for _, r := range cp {
		if !r.Enabled {
			continue
		}
		fmt.Fprintf(b, "  -- rule: %s (priority=%d)\n", sanitizeComment(r.Name), r.Priority)
		b.WriteString("  if (function()\n")
		for _, c := range r.Conditions {
			b.WriteString("    if not (")
			b.WriteString(emitCondition(c))
			b.WriteString(") then return false end\n")
		}
		b.WriteString("    return true\n  end)() then\n")
		b.WriteString(emitTarget(r.Target, opts))
		b.WriteString("  end\n")
	}

	// Fallthrough: nothing classified or routed this message. Force
	// tenant='default' so it lands in a single 'default@<domain>' queue
	// instead of inheriting whatever stale tenant kumomta might infer.
	// mailclass is left blank — the Logs UI shows '—' for unclassified rows.
	b.WriteString(`  if not msg:get_meta('tenant') or msg:get_meta('tenant') == '' then
    msg:set_meta('tenant', 'default')
  end
`)

	// One-queue-per-VMTA: collapse the queue key to the resolved egress pool
	// name. Resolution mirrors get_queue_config so the two stay in sync —
	// tenant → CLASS_TO_POOL → tenant-as-pool → 'default'. Kumomta sees a
	// single-segment queue and calls get_queue_config with tenant=nil, which
	// the renderer's queue-config block already handles.
	if gs.QueuePerVmta {
		b.WriteString(`  do
    local t = msg:get_meta('tenant') or ''
    local p = CLASS_TO_POOL[t]
    if not p or p == '' then p = t end
    if not p or p == '' then p = 'default' end
    msg:set_meta('queue', p)
  end
`)
	}

	b.WriteString("end\n\n")

	// Hook routing into both the SMTP and HTTP entry points. KumoMTA allows
	// only one handler per event, so any inbound-only pre-processing (rspamd
	// scan) is invoked from inside the single SMTP handler, before routing,
	// rather than as a second kumo.on(...). A scan that rejects/defers raises
	// and unwinds before route_message runs. The HTTP path never scans
	// (API-injected mail is not inbound).
	if rspamdEnabled(gs) {
		b.WriteString(`kumo.on('smtp_server_message_received', function(msg) iris_rspamd_scan(msg) route_message(msg) end)
kumo.on('http_message_generated',         function(msg) route_message(msg) end)

`)
	} else {
		b.WriteString(`kumo.on('smtp_server_message_received', function(msg) route_message(msg) end)
kumo.on('http_message_generated',         function(msg) route_message(msg) end)

`)
	}

	// DKIM signing + VERP rewrite. Both run at outbound dispatch and need to
	// share a hook because (a) kumomta runs handlers in registration order
	// and we must DKIM-sign with the *original* envelope-sender domain
	// before VERP rewrites MAIL FROM, and (b) keeping them in one block
	// makes that ordering self-evident at review time.
	b.WriteString(`-- ===== DKIM signing + VERP rewrite (outbound) =====
kumo.on('smtp_client_message_sending', function(msg)
  local s = msg:sender()
  local d = s and s.domain or nil
  -- DKIM first: signing depends on the From-domain, which here is read
  -- from the envelope sender (set at injection from the SMTP MAIL FROM).
  -- Must finish before any rewrite that changes the envelope sender.
  if d and d ~= '' then
    local k = DKIM_BY_DOMAIN[d]
    if k then
      if k.algo == 'ed25519' then
        msg:dkim_sign(kumo.dkim.ed25519_signer { domain = d, selector = k.selector, key = k.key })
      else
        msg:dkim_sign(kumo.dkim.rsa_sha256_signer { domain = d, selector = k.selector, key = k.key })
      end
    end
  end
`)
	if bouncePipelineEnabled(gs) {
		// VERP MAIL FROM rewrite. Token format matches pkg/verp.Encode
		// (hex16 of HMAC-SHA256(secret, msgid), then "." then msgid). The
		// per-sender bounce domain comes from BOUNCE_SENDER_TO_BOUNCE so
		// DMARC's relaxed alignment holds against the From: domain;
		// senders not in the map fall back to BOUNCE_DOMAIN_FALLBACK
		// (legacy single-domain mode); senders matching neither are left
		// unrewritten — better than producing an unaligned MAIL FROM for
		// a domain we don't host.
		//
		// Skip when sender is empty (DSN passthrough, per RFC 3464: DSNs
		// themselves carry MAIL FROM:<>) — VERPing those would create a
		// self-addressed bounce loop.
		b.WriteString(`  do
    local from_addr = (s and s.email) or ''
    local from_dom = (s and s.domain or ''):lower()
    if from_addr ~= '' then
      local bounce_dom = BOUNCE_SENDER_TO_BOUNCE[from_dom]
      if not bounce_dom or bounce_dom == '' then
        bounce_dom = BOUNCE_DOMAIN_FALLBACK
      end
      if bounce_dom and bounce_dom ~= '' then
        local mid = msg:id()
        if mid and mid ~= '' then
          local mac = kumo.digest.hmac_sha256({ key_data = BOUNCE_VERP_SECRET }, mid)
          local prefix = string.sub(tostring(mac), 1, 16)
          msg:set_sender(string.format('b+%s.%s@%s', prefix, mid, bounce_dom))
        end
      end
    end
  end
`)
	}
	b.WriteString(`end)

`)
}

// --- emit one routing-rule condition or target -----------------------------

// emitCondition produces a Lua boolean expression for a single condition.
// Both `field` and `op` are pre-validated against allow-lists upstream.
func emitCondition(c RuleCondition) string {
	var subj string
	switch c.Field {
	case "from":
		subj = "((msg:sender() and msg:sender().email) or ''):lower()"
	case "from_domain":
		subj = "((msg:sender() and msg:sender().domain) or ''):lower()"
	case "to":
		subj = "((msg:recipient() and msg:recipient().email) or ''):lower()"
	case "to_domain":
		subj = "((msg:recipient() and msg:recipient().domain) or ''):lower()"
	case "header.subject":
		subj = "(msg:get_first_named_header_value('subject') or '')"
	case "source_ip":
		// kumomta exposes the peer address via the message's received_from
		// meta; tostring() makes the comparison string-safe.
		subj = "(msg:get_meta('received_from') or '')"
	default:
		return "false"
	}
	val := MustLuaString(strings.ToLower(c.Value))
	switch c.Op {
	case "equals":
		return fmt.Sprintf("(%s == %s)", subj, val)
	case "contains":
		return fmt.Sprintf("(string.find(%s, %s, 1, true) ~= nil)", subj, val)
	case "startswith":
		return fmt.Sprintf("(%s:sub(1, #%s) == %s)", subj, val, val)
	case "endswith":
		return fmt.Sprintf("(%s:sub(-#%s) == %s)", subj, val, val)
	case "regex":
		// Lua patterns are not full regex; we still validated the value as a
		// Go-RE2 pattern upstream so authors got early feedback. The runtime
		// match here uses string.find with the value as a Lua pattern, which
		// covers the simple cases authors typically write.
		return fmt.Sprintf("(string.find(%s, %s) ~= nil)", subj, val)
	}
	return "false"
}

// emitTarget produces the Lua statement(s) executed when a rule matches. The
// dry-run prefix is interleaved with the real action so operators see both
// the intent and the effect in the kumomta log.
func emitTarget(t RuleTarget, opts RenderOptions) string {
	dry := ""
	if opts.DryRun {
		dry = "    print('[DRY-RUN] target=" + sanitizeComment(t.Kind) + "/" + sanitizeComment(t.Ref) + "')\n"
	}
	switch t.Kind {
	case "vmta", "vmta_group":
		// Both kinds resolve through the same POOLS table; tenant=pool_name.
		return dry + fmt.Sprintf("    msg:set_meta('tenant', %s)\n    return\n", MustLuaString(t.Ref))
	case "queue":
		// Direct queue override — bypasses the tenant→pool indirection.
		return dry + fmt.Sprintf("    msg:set_meta('queue', %s)\n    return\n", MustLuaString(t.Ref))
	case "reject":
		text := t.RejectText
		if text == "" {
			text = "rejected by routing rule"
		}
		return dry + fmt.Sprintf("    kumo.reject(%d, %s)\n    return\n", t.RejectCode, MustLuaString(text))
	case "discard":
		// Ack with 250 OK so the sender sees success, but route to the null
		// queue which kumomta treats as "drop on the floor". Operators who
		// want a hard reject should use `reject` instead.
		return dry + "    kumo.reject(250, 'discarded by policy')\n    return\n"
	}
	return ""
}

// --- footer -----------------------------------------------------------------

func writeFooter(b *strings.Builder, opts RenderOptions) {
	b.WriteString("\n-- end of generated policy\n")
}

// --- helpers (kept so tests + other writers reuse them) --------------------

// sanitizeComment strips control bytes so a malicious comment cannot
// terminate the comment context. Lua "--" comments end at end-of-line, so
// the only real risk is newline injection.
func sanitizeComment(s string) string {
	if s == "" {
		return ""
	}
	r := strings.NewReplacer("\n", " ", "\r", " ", "\x00", "")
	return r.Replace(s)
}

// toIdent converts a name into a safe Lua identifier; falls back to "x".
// Retained for backward compatibility with callers outside this file.
func toIdent(s string) string {
	if s == "" {
		return "x"
	}
	var b strings.Builder
	b.Grow(len(s) + 1)
	if c := s[0]; !((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || c == '_') {
		b.WriteByte('_')
	}
	for i := 0; i < len(s); i++ {
		c := s[i]
		switch {
		case (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || c == '_':
			b.WriteByte(c)
		case c >= '0' && c <= '9':
			b.WriteByte(c)
		default:
			b.WriteByte('_')
		}
	}
	return b.String()
}
