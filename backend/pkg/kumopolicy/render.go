const webhookTracker = "iris_webhook"

// writeWebhookConsts emits the lookup tables and the WEBHOOK_TRACKER queue
// name. Exact-address webhooks key WEBHOOK_BY_EMAIL; bare-domain catch-alls
// key WEBHOOK_BY_DOMAIN. Both are declared even when empty so later closures
// resolve them as upvalues.
func writeWebhookConsts(b *strings.Builder, whs []MailWebhook) {
	whs = enabledWebhooks(whs)
	b.WriteString("-- ===== inbound mail webhooks =====\n")
	fmt.Fprintf(b, "local WEBHOOK_TRACKER = %s\n", MustLuaString(webhookTracker))
	b.WriteString("local WEBHOOK_BY_EMAIL = {}\n")
	b.WriteString("local WEBHOOK_BY_DOMAIN = {}\n")
	for _, w := range whs {
		table := "WEBHOOK_BY_EMAIL"
		key := strings.ToLower(w.Address)
		if w.IsDomainCatchAll() {
			table = "WEBHOOK_BY_DOMAIN"
			key = w.WebhookDomain()
		}
		fmt.Fprintf(b, "%s[%s] = { url = %s, secret = %s }\n",
			table, MustLuaString(key), MustLuaString(w.URL), MustLuaString(w.Secret))
	}
	b.WriteString("\n")
}

// writeWebhookCatcher emits the custom_lua queue constructor that POSTs the
// raw message to the matched endpoint. A 2xx acks (250); anything else (or a
// transport error) defers so kumomta retries on its normal backoff.
func writeWebhookCatcher(b *strings.Builder, whs []MailWebhook) {
	if len(enabledWebhooks(whs)) == 0 {
		return
	}
	b.WriteString(`kumo.on('make.webhook_post', function(_domain, _tenant, _campaign)
  local connection = {}
  function connection:send(message)
    local rcpt = message:recipient()
    local email = (rcpt and rcpt.email or ''):lower()
    local dom = (rcpt and rcpt.domain or ''):lower()
    local route = WEBHOOK_BY_EMAIL[email] or WEBHOOK_BY_DOMAIN[dom]
    if not route then
      return '250 no webhook route (dropped)'
    end
    local body = message:get_data()
    local client = kumo.http.build_client {}
    local req = client:post(route.url)
    req:header('Content-Type', 'message/rfc822')
    req:header('X-Iris-Recipient', email)
    req:header('X-Iris-Message-Id', tostring(message:id()))
    if route.secret and route.secret ~= '' then
      local sig = kumo.digest.hmac_sha256({ key_data = route.secret }, body)
      req:header('X-Iris-Signature', tostring(sig))
    end
    req:body(body)
    local ok, resp = pcall(function() return req:send() end)
    if not ok then
      kumo.log_error('webhook: post failed err=' .. tostring(resp))
      return string.format('451 4.4.1 webhook post error: %s', tostring(resp))
    end
    local code = resp:status_code()
    if code >= 200 and code < 300 then
      return string.format('250 forwarded to webhook (%d)', code)
    end
    return string.format('451 4.4.1 webhook returned %d', code)
  end
  return connection
end)

`)
}

// --- inbound rspamd spam filtering ------------------------------------------

// rspamdEnabled reports whether inbound scanning is configured (mode tag or
// enforce + a URL).
func rspamdEnabled(gs GlobalSettings) bool {
	m := strings.ToLower(strings.TrimSpace(gs.RspamdMode))
	return (m == "tag" || m == "enforce") && strings.TrimSpace(gs.RspamdURL) != ""
}

// rspamdEnforce reports whether rspamd's reject/defer verdicts are honored
// (vs tag-only).
func rspamdEnforce(gs GlobalSettings) bool {
	return strings.ToLower(strings.TrimSpace(gs.RspamdMode)) == "enforce"
}

// writeRspamdConsts emits the rspamd endpoint + enforce flag (only when
// enabled). Declared early so the reception hook can capture them.
func writeRspamdConsts(b *strings.Builder, gs GlobalSettings) {
	if !rspamdEnabled(gs) {
		return
	}
	b.WriteString("-- ===== inbound spam filtering (rspamd) =====\n")
	fmt.Fprintf(b, "local RSPAMD_URL = %s\n", MustLuaString(strings.TrimSpace(gs.RspamdURL)))
	fmt.Fprintf(b, "local RSPAMD_ENFORCE = %t\n\n", rspamdEnforce(gs))
}

// writeRspamdHook defines the iris_rspamd_scan(msg) Lua function that scans
// inbound mail for hosted domains through rspamd's /checkv2 and honors the
// verdict. KumoMTA permits only ONE handler per event, so this is NOT its own
// kumo.on('smtp_server_message_received', …) — instead the single routing-chain
// handler calls iris_rspamd_scan BEFORE route_message (see writeRoutingChain),
// so a reject/defer happens before any webhook/DSN handling. It's wired only
// into the SMTP path, not http_message_generated, so API-injected mail is
// never scanned. Fails open: if rspamd is unreachable or its response can't be
// parsed, the message is accepted.
func writeRspamdHook(b *strings.Builder, gs GlobalSettings) {
	if !rspamdEnabled(gs) {
		return
	}
	b.WriteString(`local function iris_rspamd_scan(msg)
  local rcpt = msg:recipient()
  local rdom = (rcpt and rcpt.domain or ''):lower()
  -- Inbound-to-hosted only: skip outbound relay (external recipient) and
  -- bounce/DSN subdomains (system mail must not be spam-rejected).
  if not (LISTENER_DOMAINS and LISTENER_DOMAINS[rdom]) then return end
  if BOUNCE_DOMAINS and BOUNCE_DOMAINS[rdom] then return end
  local s = msg:sender()
  local client = kumo.http.build_client {}
  local req = client:post(RSPAMD_URL .. '/checkv2')
  req:header('From', (s and s.email) or '')
  req:header('Rcpt', (rcpt and rcpt.email) or '')
  req:header('Deliver-To', (rcpt and rcpt.email) or '')
  local ok_ip, ip = pcall(function() return msg:get_meta('received_from') end)
  if ok_ip and ip and ip ~= '' then req:header('Ip', tostring(ip)) end
  req:body(msg:get_data())
  local ok, resp = pcall(function() return req:send() end)
  if not ok then
    kumo.log_error('rspamd: request failed: ' .. tostring(resp))
    return -- fail-open
  end
  if resp:status_code() ~= 200 then
    kumo.log_error('rspamd: unexpected status ' .. tostring(resp:status_code()))
    return -- fail-open
  end
  local ok2, r = pcall(function() return kumo.serde.json_parse(resp:text()) end)
  if not ok2 or type(r) ~= 'table' then
    return -- fail-open
  end
  local score = r.score or 0
  local action = r.action or 'no action'
  msg:prepend_header('X-Spam-Score', string.format('%.2f', score))
  msg:prepend_header('X-Rspamd-Action', tostring(action))
  if action == 'reject' then
    if RSPAMD_ENFORCE then
      kumo.reject(550, '5.7.1 message rejected as spam')
      return
    end
    msg:prepend_header('X-Spam', 'yes')
  elseif action == 'soft reject' or action == 'greylist' then
    if RSPAMD_ENFORCE then
      kumo.reject(451, '4.7.1 greylisted, please try again later')
      return
    end
  elseif action == 'add header' or action == 'rewrite subject' then
    msg:prepend_header('X-Spam', 'yes')
    msg:prepend_header('X-Spam-Status', string.format('Yes, score=%.2f', score))
  end
end

`)
}

// --- listener domains -------------------------------------------------------

func writeListenerDomains(b *strings.Builder, ls []Listener, gs GlobalSettings, whs []MailWebhook) {
	// Aggregate domain → cfg across all listeners. If the same domain appears
	// on multiple listeners with different settings the last wins; the admin
	// service should prevent that upstream.
	type rule struct {
		relay     bool
		requireTL bool
	}
	domains := map[string]rule{}
	for _, l := range ls {
		for _, d := range l.Domains {
			domains[d.Domain] = rule{relay: d.RelayAllowed, requireTL: d.RequireTLS}
		}
	}
	// Each bounce domain is a hostname we accept inbound DSNs at. In
	// multi-domain mode that's one entry per sender (bounces.test-1.com,
	// bounces.test2.com, …); in legacy mode it's the single fallback.
	// Operators don't have to add these as Listener rows in the UI —
	// the env-driven config wires them implicitly.
	for _, d := range effectiveBounceDomains(gs) {
		domains[d] = rule{relay: true, requireTL: false}
	}
	// Accept inbound mail for every webhook recipient's domain so kumomta
	// relays it into the chain (where route_message hands it to the webhook
	// queue) instead of rejecting it as an unknown destination.
	for _, w := range enabledWebhooks(whs) {
		domains[w.WebhookDomain()] = rule{relay: true, requireTL: false}
	}
	if len(domains) == 0 {
		return
	}
	keys := make([]string, 0, len(domains))
	for k := range domains {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	b.WriteString("-- ===== listener domains =====\n")
	b.WriteString("local LISTENER_DOMAINS = {\n")
	for _, k := range keys {
		r := domains[k]
		// require_tls is enforced via listener-level TLS config in
		// kumomta, not on make_listener_domain (which doesn't accept
		// the field). We keep the column on the data model so future
		// versions of kumomta — or our own admission check — can
		// surface it; for now it's a no-op in the rendered Lua.
		_ = r.requireTL
		fmt.Fprintf(b, "  [%s] = { relay_to = %t },\n",
			MustLuaString(k), r.relay)
	}
	b.WriteString("}\n\n")
	b.WriteString(`kumo.on('get_listener_domain', function(domain, listener)
  local cfg = LISTENER_DOMAINS[domain]
  if not cfg then return nil end
  return kumo.make_listener_domain {
    relay_to = cfg.relay_to,
  }
end)

`)
}

