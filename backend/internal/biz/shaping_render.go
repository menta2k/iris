package biz

import (
	"fmt"
	"regexp"
	"sort"
	"strconv"
	"strings"
)

// RenderBaseShaping serializes active delivery blueprints into a KumoMTA shaping
// TOML document (the base layer the shaping helper loads). Each blueprint is a
// domain block grouped to its provider site via mx_rollup; the warmup engine and
// TSA layer per-IP overrides on top via separately-loaded files.
//
// Only set fields are emitted. Blocks are sorted by MX pattern for a stable,
// checksummable document.
func RenderBaseShaping(blueprints []*DeliveryBlueprint) string {
	rows := make([]*DeliveryBlueprint, 0, len(blueprints))
	for _, b := range blueprints {
		if b != nil && b.Status == BlueprintActive && strings.TrimSpace(b.MXPattern) != "" {
			rows = append(rows, b)
		}
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].MXPattern < rows[j].MXPattern })

	var b strings.Builder
	b.WriteString("# Generated by iris — base delivery blueprints. Do not edit by hand.\n")
	for _, r := range rows {
		// Top-level domain block keyed by the recipient MX pattern; mx_rollup=false
		// so it matches that exact recipient domain (verified against kumod — a
		// [domain."x"] table or mx_rollup grouping does NOT match the domain arg).
		fmt.Fprintf(&b, "\n[%s]\n", tomlQuote(r.MXPattern))
		b.WriteString("mx_rollup = false\n")
		if rate := strings.TrimSpace(r.ConnRate); rate != "" {
			fmt.Fprintf(&b, "max_connection_rate = %s\n", tomlQuote(rate))
		}
		if r.DeliveriesPerConn > 0 {
			fmt.Fprintf(&b, "max_deliveries_per_connection = %d\n", r.DeliveriesPerConn)
		}
		if r.ConnLimit > 0 {
			fmt.Fprintf(&b, "connection_limit = %d\n", r.ConnLimit)
		}
		if r.DailyCap > 0 {
			fmt.Fprintf(&b, "max_message_rate = %s\n", tomlQuote(strconv.Itoa(r.DailyCap)+"/day"))
		}
	}
	return b.String()
}

// RenderWarmupShaping serializes per-IP warmup daily-cap overrides into a shaping
// TOML document, loaded after the base so it overrides the base max_message_rate
// for the warming source. rates is source -> bucket -> "N/day"; buckets map to
// provider MX patterns via providerForBucket. Empty when no warmup is active.
func RenderWarmupShaping(rates map[string]map[string]string) string {
	// Group overrides by recipient domain so each domain block is emitted once
	// with mx_rollup=false (matching the base block's keying so the per-source
	// override merges over it — verified against kumod; without the matching
	// mx_rollup the override silently does not apply).
	type override struct{ source, rate string }
	byDomain := map[string][]override{}
	for src, buckets := range rates {
		for bucket, rate := range buckets {
			if rate == "" {
				continue
			}
			for _, mx := range providerMXPatterns(bucket) {
				byDomain[mx] = append(byDomain[mx], override{src, rate})
			}
		}
	}
	if len(byDomain) == 0 {
		return "# Generated by iris — warmup overrides (none active).\n"
	}
	domains := make([]string, 0, len(byDomain))
	for d := range byDomain {
		domains = append(domains, d)
	}
	sort.Strings(domains)

	var b strings.Builder
	b.WriteString("# Generated by iris — per-IP warmup overrides. Do not edit by hand.\n")
	for _, mx := range domains {
		fmt.Fprintf(&b, "\n[%s]\nmx_rollup = false\n", tomlQuote(mx))
		ovs := byDomain[mx]
		sort.Slice(ovs, func(i, j int) bool { return ovs[i].source < ovs[j].source })
		for _, o := range ovs {
			fmt.Fprintf(&b, "\n[%s.sources.%s]\n", tomlQuote(mx), tomlQuote(o.source))
			fmt.Fprintf(&b, "max_message_rate = %s\n", tomlQuote(o.rate))
		}
	}
	return b.String()
}

// RenderAutomation serializes operator-authored TSA automation rules into a
// shaping TOML document of [["<domain>".automation]] blocks. This file is loaded
// by the TSA daemon (not kumod); the daemon evaluates the rules against live
// delivery events and publishes the resulting back-off, which kumod merges under
// the warmup ceiling. Rules are emitted in a stable (domain, regex) order.
func RenderAutomation(rules []*AutomationRule) string {
	active := make([]*AutomationRule, 0, len(rules))
	for _, r := range rules {
		if r != nil && r.Status == AutomationActive && strings.TrimSpace(r.Domain) != "" {
			active = append(active, r)
		}
	}
	sort.Slice(active, func(i, j int) bool {
		if active[i].Domain != active[j].Domain {
			return active[i].Domain < active[j].Domain
		}
		return active[i].Regex < active[j].Regex
	})

	var b strings.Builder
	b.WriteString("# Generated by iris — TSA automation rules. Do not edit by hand.\n")
	for _, r := range active {
		fmt.Fprintf(&b, "\n[[%s.automation]]\n", tomlQuote(r.Domain))
		// Triple-quoted literal so regex backslashes need no escaping (Validate
		// rejects a regex containing a triple quote).
		fmt.Fprintf(&b, "regex = '''%s'''\n", r.Regex)
		b.WriteString("action = " + automationActionExpr(r) + "\n")
		if r.Trigger == "immediate" || r.Trigger == "" {
			b.WriteString("trigger = \"Immediate\"\n")
		} else {
			fmt.Fprintf(&b, "trigger = {Threshold=%s}\n", tomlQuote(r.Trigger))
		}
		fmt.Fprintf(&b, "duration = %s\n", tomlQuote(r.Duration))
	}
	return b.String()
}

// mergeAutomation concatenates operator-authored automation rules with the ones
// compiled from bounce rules into a fresh slice (never mutating either input).
func mergeAutomation(operator, compiled []*AutomationRule) []*AutomationRule {
	out := make([]*AutomationRule, 0, len(operator)+len(compiled))
	out = append(out, operator...)
	out = append(out, compiled...)
	return out
}

// BounceRulesToAutomation compiles active throttle / suspend_domain bounce-action
// rules into TSA automation rules so the config generator renders them into
// iris-automation.toml and the TSA daemon enforces them reactively. Every
// compiled rule carries a threshold trigger and a bounded duration, so a single
// bounce never indefinitely halts a provider. retry / suppress rules produce no
// shaping (they are enforced elsewhere).
func BounceRulesToAutomation(rules []*BounceActionRule) []*AutomationRule {
	var out []*AutomationRule
	for _, r := range rules {
		if r == nil || (r.Status != "" && r.Status != "active") {
			continue
		}
		var action, cfgName, cfgValue, duration string
		switch r.Action {
		case BounceActionSuspendDomain:
			action = AutomationSuspend
			duration = firstNonEmpty(r.ActionConfig, "1h")
		case BounceActionThrottle:
			action = AutomationSetConfig
			cfgName, cfgValue = parseThrottleConfig(r.ActionConfig)
			duration = "1h"
		default:
			continue // retry / suppress → no shaping
		}
		regex := bounceRuleRegex(r)
		if regex == "" {
			continue
		}
		for _, domain := range bounceProviderDomains(r.Provider) {
			out = append(out, &AutomationRule{
				Domain:      domain,
				Regex:       regex,
				Action:      action,
				ConfigName:  cfgName,
				ConfigValue: cfgValue,
				Trigger:     "2/hr", // safety: act only after repeated hits
				Duration:    duration,
				Status:      AutomationActive,
			})
		}
	}
	return out
}

// bounceRuleRegex builds a Rust-regex that matches the SMTP response for a bounce
// rule, preferring the most specific available signal. Literal text is escaped so
// the operator's plain pattern/code is matched verbatim.
func bounceRuleRegex(r *BounceActionRule) string {
	switch {
	case r.Pattern != "":
		return regexp.QuoteMeta(r.Pattern)
	case r.EnhancedCode != "":
		return regexp.QuoteMeta(r.EnhancedCode)
	case r.SMTPCode != "":
		return r.SMTPCode
	default:
		return ""
	}
}

// parseThrottleConfig splits a throttle ActionConfig ("name=value", e.g.
// "max_message_rate=100/h") into a SetConfig name/value, defaulting to a
// conservative message-rate cap when unset or malformed.
func parseThrottleConfig(cfg string) (name, value string) {
	if i := strings.Index(cfg, "="); i >= 0 {
		name = strings.TrimSpace(cfg[:i])
		value = strings.TrimSpace(cfg[i+1:])
	}
	if name == "" {
		name = "max_message_rate"
	}
	if value == "" {
		value = "100/h"
	}
	return name, value
}

// bounceProviderDomains maps a bounce-rule provider bucket to the receiving-MX
// domain patterns a TSA automation rule keys on. "" (all providers) maps to the
// "default" block; an unknown bucket is used verbatim as a best-effort domain.
func bounceProviderDomains(provider string) []string {
	switch provider {
	case "gmail":
		return providerMXPatterns(MBPGmail)
	case "microsoft":
		return providerMXPatterns(MBPMicrosoft)
	case "yahoo":
		return providerMXPatterns(MBPYahoo)
	case "":
		return []string{"default"}
	default:
		return []string{provider}
	}
}

func firstNonEmpty(a, b string) string {
	if strings.TrimSpace(a) != "" {
		return a
	}
	return b
}

func automationActionExpr(r *AutomationRule) string {
	switch r.Action {
	case AutomationSuspend:
		return `"Suspend"`
	case AutomationSuspendTenant:
		return `"SuspendTenant"`
	case AutomationSetConfig:
		return "{SetConfig={name=" + tomlQuote(r.ConfigName) + ", value=" + tomlQuote(r.ConfigValue) + "}}"
	default:
		return `"Suspend"`
	}
}

// providerMXPatterns maps an MBP bucket to the representative MX patterns the
// warmup override applies to. Mirrors the seed registry; the "default" bucket
// has no specific domain (handled by the base fallback), so it emits nothing.
func providerMXPatterns(bucket string) []string {
	switch bucket {
	case MBPGmail:
		// gmail.com is the domain the overwhelming majority of Gmail recipients
		// use; google.com/googlemail.com are corporate/legacy. With mx_rollup=false
		// the block matches the literal recipient domain, so gmail.com MUST be here
		// or real Gmail traffic escapes the warmup ceiling entirely.
		return []string{"gmail.com", "google.com", "googlemail.com"}
	case MBPMicrosoft:
		return []string{"outlook.com", "hotmail.com", "live.com", "office365.com", "msn.com"}
	case MBPYahoo:
		return []string{"yahoodns.net", "yahoo.com", "ymail.com", "aol.com"}
	default:
		return nil
	}
}

// tomlQuote returns a TOML basic-string literal, escaping backslashes and quotes.
func tomlQuote(s string) string {
	r := strings.NewReplacer(`\`, `\\`, `"`, `\"`)
	return `"` + r.Replace(s) + `"`
}
