package biz

import (
	"context"
	"regexp"
	"strings"
	"time"
	"unicode"
)

// Classification rule sources.
const (
	ClassificationSourceManual = "manual" // operator-authored
	ClassificationSourceAI     = "ai"     // generated by the LLM fallback
)

// Classification match types. A rule either matches by trigram similarity of
// the normalized subject (the default, self-populating corpus) or by an
// operator-authored regular expression evaluated against the raw subject.
const (
	ClassificationMatchSimilarity = "similarity"
	ClassificationMatchRegex      = "regex"
)

// maxClassificationPattern bounds a regex pattern so a pathological rule can't
// be stored. Regex rules are few and operator-authored.
const maxClassificationPattern = 512

// SubjectClassification is one normalized-subject → label rule. It doubles as
// the similarity-match corpus and the AI-result cache: operator-authored rows
// (source "manual") seed it, and AI-labeled rows (source "ai") accumulate as new
// subjects are seen, so LLM calls taper off over time.
type SubjectClassification struct {
	ID string
	// Subject is a representative raw subject (or an operator-entered phrase). It
	// is the only place a raw subject is persisted; mail_records never stores it.
	Subject string
	// SubjectNormalized is the matching key (see NormalizeSubject).
	SubjectNormalized string
	// Label is the classification, at most two words. "" means pending.
	Label  string
	Source string
	// MatchType selects how this rule matches an incoming subject:
	// ClassificationMatchSimilarity (trigram, default) or
	// ClassificationMatchRegex (Subject holds the pattern).
	MatchType string
	// Priority orders evaluation across all rules: higher is matched first and
	// the first match wins. Default 0.
	Priority  int32
	HitCount  int64
	CreatedAt time.Time
	UpdatedAt time.Time
}

// CompileRegex compiles a regex rule's pattern. It is only meaningful when
// MatchType is ClassificationMatchRegex; callers guard on that.
func (c *SubjectClassification) CompileRegex() (*regexp.Regexp, error) {
	return regexp.Compile(c.Subject)
}

// ClassifyPolicy is the runtime state of the subject-classification feature,
// read from global settings on each use so operators can toggle/tune it live.
type ClassifyPolicy struct {
	Enabled   bool
	Model     string
	Threshold float64
	APIBase   string
}

// ClassifyPolicyProvider supplies the current classification policy. Satisfied
// by GlobalSettingsUsecase.
type ClassifyPolicyProvider interface {
	ClassifyPolicyNow(ctx context.Context) ClassifyPolicy
}

// SubjectAIClassifier labels a subject via an LLM, returning a short (≤2 word)
// category. Satisfied by OpenAIClassifier; nil when no API key is configured.
type SubjectAIClassifier interface {
	ClassifySubject(ctx context.Context, subject, model, apiBase string) (string, error)
}

// SubjectClassificationRepo is the persistence boundary for classification rules.
type SubjectClassificationRepo interface {
	// BestMatch returns the labeled similarity rule matching normalized at or
	// above threshold (pg_trgm), preferring higher priority then higher
	// similarity, or (nil, nil) when nothing qualifies.
	BestMatch(ctx context.Context, normalized string, threshold float64) (*SubjectClassification, error)
	// RegexRules returns all labeled regex rules ordered by priority (highest
	// first) so the matcher can evaluate them against a raw subject.
	RegexRules(ctx context.Context) ([]*SubjectClassification, error)
	// Upsert inserts or updates by subject_normalized, returning the stored row.
	Upsert(ctx context.Context, c *SubjectClassification) (*SubjectClassification, error)
	IncrementHit(ctx context.Context, id string) error
	List(ctx context.Context) ([]*SubjectClassification, error)
	Create(ctx context.Context, c *SubjectClassification) (*SubjectClassification, error)
	Update(ctx context.Context, c *SubjectClassification) (*SubjectClassification, error)
	Delete(ctx context.Context, id string) error
}

// subjPrefixRe strips a leading Re:/Fwd:/Fw: (possibly repeated) reply/forward
// marker before normalization.
var subjPrefixRe = regexp.MustCompile(`(?i)^\s*(re|fwd|fw)\s*:\s*`)

// NormalizeSubject reduces a raw subject to a stable matching key: lowercase,
// strip reply/forward prefixes, then keep only letters (Unicode-aware) with
// every run of digits/punctuation/whitespace collapsed to a single space. This
// makes per-message noise (order numbers, names, tracking codes) fall away so
// "Your order #12345 shipped" and "Your order #67890 shipped" collapse to the
// same key ("your order shipped"). Deterministic.
func NormalizeSubject(s string) string {
	s = strings.ToLower(strings.TrimSpace(s))
	for {
		t := strings.TrimSpace(subjPrefixRe.ReplaceAllString(s, ""))
		if t == s {
			break
		}
		s = t
	}
	var b strings.Builder
	space := true // suppress leading space
	for _, r := range s {
		if unicode.IsLetter(r) {
			b.WriteRune(r)
			space = false
		} else if !space {
			b.WriteByte(' ')
			space = true
		}
	}
	return strings.TrimSpace(b.String())
}

// normalizeLabel coerces a raw LLM/operator label to at most two lowercase
// words (letters and hyphens only), dropping punctuation and extra words.
func normalizeLabel(s string) string {
	s = strings.ToLower(strings.TrimSpace(s))
	var b strings.Builder
	space := true
	for _, r := range s {
		if unicode.IsLetter(r) || r == '-' {
			b.WriteRune(r)
			space = false
		} else if !space {
			b.WriteByte(' ')
			space = true
		}
	}
	fields := strings.Fields(b.String())
	if len(fields) > 2 {
		fields = fields[:2]
	}
	return strings.Join(fields, " ")
}

// Validate normalizes and checks an operator-authored classification rule.
func (c *SubjectClassification) Validate() error {
	if c.MatchType == "" {
		c.MatchType = ClassificationMatchSimilarity
	}
	c.Subject = strings.TrimSpace(c.Subject)
	if c.Subject == "" {
		return Invalid("CLASSIFICATION_SUBJECT_REQUIRED", "subject must not be empty")
	}

	switch c.MatchType {
	case ClassificationMatchSimilarity:
		if len(c.Subject) > 998 { // RFC 5322 line limit
			return Invalid("CLASSIFICATION_SUBJECT_TOO_LONG", "subject must be at most 998 characters")
		}
		c.SubjectNormalized = NormalizeSubject(c.Subject)
		if c.SubjectNormalized == "" {
			return Invalid("CLASSIFICATION_SUBJECT_EMPTY_KEY", "subject has no classifiable text after normalization")
		}
	case ClassificationMatchRegex:
		if len(c.Subject) > maxClassificationPattern {
			return Invalid("CLASSIFICATION_PATTERN_TOO_LONG", "pattern must be at most 512 characters")
		}
		if _, err := c.CompileRegex(); err != nil {
			return Invalid("CLASSIFICATION_PATTERN_INVALID", "pattern is not a valid regular expression: %s", err.Error())
		}
		// Regex rules carry no similarity key; keep it empty so the partial
		// unique index (similarity rows only) never trips on them.
		c.SubjectNormalized = ""
	default:
		return Invalid("CLASSIFICATION_MATCH_TYPE_INVALID", "match_type must be similarity or regex")
	}

	c.Label = normalizeLabel(c.Label)
	if c.Label == "" {
		return Invalid("CLASSIFICATION_LABEL_REQUIRED", "label must be one or two words")
	}
	if c.Source == "" {
		c.Source = ClassificationSourceManual
	}
	if c.Source != ClassificationSourceManual && c.Source != ClassificationSourceAI {
		return Invalid("CLASSIFICATION_SOURCE_INVALID", "source must be manual or ai")
	}
	return nil
}
